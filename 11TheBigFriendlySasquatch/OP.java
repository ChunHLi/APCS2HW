import java.util.*;

public class OP{
    int row;
    int col;

    public OP(int Row, int Col){
	row = Row;
	col = Col;
    }
    
    public int getRow(){
	return row;
    }
    
    public int getCol(){
	return col;
    }

    public void setRow(int Row){
	row = Row;
    }
    
    public void setCol(int Col){
	col = Col;
    }

}
